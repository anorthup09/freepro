async function seedCrew() {
  let inserted = 0, skipped = 0;
  for (const m of crew) {
    const existing = await sql`SELECT id FROM crew_members WHERE name = ${m.name} LIMIT 1`;
    if (existing.length > 0) { skipped++; continue; }
    await sql`
      INSERT INTO crew_members (id, name, email, phone, company, home_airport, date_of_birth, passport_number, passport_expiry, known_traveler_number, seat_preference, notes, is_active)
      VALUES (
        gen_random_uuid()::text,
        ${m.name},
        ${m.email || null},
        ${m.phone || null},
        ${m.company || null},
        ${m.homeAirport || null},
        ${m.dob || null},
        ${m.passport ? String(m.passport) : null},
        ${m.passportExp || null},
        ${m.ktn ? String(m.ktn) : null},
        ${m.seat || null},
        ${m.notes || null},
        TRUE
      )`;
    inserted++;
  }
  console.log(`Crew seed: ${inserted} inserted, ${skipped} skipped (already exist).`);
}

